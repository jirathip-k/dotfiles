local comma = {
	[[            ]],
	[[            ]],
	[[            ]],
	[[            ]],
	[[▄█╗         ]],
	[[╚═╝         ]],
}

local space = "      "

local combine = function(table1, table2, seperate)
	local result = {}
	for i = 1, 6 do
		result[i] = table1[i] .. (seperate and space or "") .. table2[i]
	end

	return result
end

local num_to_ascii = function(number)
	if number < 10 then
		return numbers[number + 1]
	end
	local ones = number % 10
	local tens = math.floor(number / 10)

	return combine(numbers[tens + 1], numbers[ones + 1])
end

return {
	"goolord/alpha-nvim",
	event = "VimEnter",
	config = function()
		local alpha = require("alpha")
		local dashboard = require("alpha.themes.dashboard")

		local date = os.date("*t")
		local get_todays_date = function()
			local result = {}
			local weekday = weekdays[date.wday]
			local month = months[date.month]
			result = combine(weekday, comma)
			result = combine(result, month)
			result = combine(result, num_to_ascii(date.day), true)
			result[7] = ""
			return result
		end
		dashboard.section.header.val = get_todays_date()

		-- Set menu
		dashboard.section.buttons.val = {
			dashboard.button("r", "  > Recent", ":Telescope oldfiles<CR>"),
			dashboard.button("f", "󰱼  > Find File", ":Telescope find_files<CR>"),
			dashboard.button("g", "  > Live Grep", ":Telescope live_grep<CR>"),
			dashboard.button("q", "  > Quit NVIM", ":qa<CR>"),
		}
		-- Send config to alpha
		alpha.setup(dashboard.opts)

		-- Disable folding on alpha buffer
		vim.cmd([[autocmd FileType alpha setlocal nofoldenable]])
	end,
}
